#include "libft.h"

void	ft_putnbr(int n)
{
	long	num;

	num = n;
	if (n < 0)
	{
		write(1, "-", 1);
		num = num * (-1);
	}
	if (num > 9)
		ft_putnbr(num / 10);
	ft_putchar('0' + num % 10);
}
