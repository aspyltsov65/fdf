#include "libft.h"

double	ft_sqrt(size_t n)
{
	double i;

	i = 1;
	while (i * i < n)
		i++;
	if (i * i == n)
		return (i);
	i--;
	while (i * i < n)
	{
		while (i * i < n)
			i += 0.01;
		i += 0.1;
	}
	i -= 0.11;
	return (i);
}
