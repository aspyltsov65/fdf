#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*j;

	if (f)
	{
		while (lst)
		{
			j = lst->next;
			f(lst);
			lst = j;
		}
	}
}
